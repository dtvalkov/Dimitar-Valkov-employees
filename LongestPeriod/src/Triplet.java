
public class Triplet<X,Y,Z> { //template class for a triple object


		  public  X x; 
		  public  Y y; 
		  public  Z z;
		  
		  
		  
		  public Triplet(X x, Y y, Z z) { 
		    this.x = x; 
		    this.y = y; 
		    this.z = z;
		  }



		public Triplet() {
			
		}
		  
		  

		  
		  
		  
		} 
	

